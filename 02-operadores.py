### Arithmetic operators ###
print(5 + 5) # Addition
print(5 - 5) # Substraction
print(5 * 5) # Multiplication
print(5 / 5) # Division
print(10 % 2) # Module
print(10 // 3) # Floor division (aproximation without decimals)
print(2 ** 3) # Exponentiation
print("-------")

print("Hello " + "World") # Concatenation
print("Hello " * 3) # Repetition
print("-------")

### Comparison operators ###
print(3 > 4) # Greater than
print(3 < 4) # Less than
print(3 >= 4) # Greater than or equal
print(3 <= 4) # Less than or equal
print(3 == 4) # Equal
print(3 != 4) # Not equal' 

print("Hello" > "World") # Alphabetical comparison
print(len("Hello") >= len("World")) # Length comparison
print("-------")

### Logical operators ###
print(3 > 4 and 4 > 3) # And
print(3 > 4 or 4 > 3) # Or
print(not 3 > 4) # Not
print("-------")